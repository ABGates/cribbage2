    //changes the suit of the addCards buttons
    private void slider_suitStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_slider_suitStateChanged
        // TODO add your handling code here:
        int suit = slider_suit.getValue();
        
        ArrayList<javax.swing.JButton> buttons = new ArrayList<javax.swing.JButton>();
        buttons.add(b_1);
        buttons.add(b_2);
        buttons.add(b_3);
        buttons.add(b_4);
        buttons.add(b_5);
        buttons.add(b_6);
        buttons.add(b_7);
        buttons.add(b_8);
        buttons.add(b_9);
        buttons.add(b_10);
        buttons.add(b_11);
        buttons.add(b_12);
        buttons.add(b_13);
        
        if(suit == 4){
            global_suit = "Spades";
            for(int i =1; i<=buttons.size(); i++){
                buttons.get(i-1).setIcon(new javax.swing.ImageIcon(getClass().getResource("/gui/deck/"+ i +"S.png"))); 
            }
        }
        else if(suit == 3){
            global_suit = "Hearts";
            for(int i =1; i<=buttons.size(); i++){
                buttons.get(i-1).setIcon(new javax.swing.ImageIcon(getClass().getResource("/gui/deck/"+ i +"H.png"))); 
            }
        }
        else if(suit == 2){
            global_suit = "Clubs";
            for(int i =1; i<=buttons.size(); i++){
                buttons.get(i-1).setIcon(new javax.swing.ImageIcon(getClass().getResource("/gui/deck/"+ i +"C.png"))); 
            }
        }
        else{
            global_suit = "Diamonds";
            for(int i =1; i<=buttons.size(); i++){
                buttons.get(i-1).setIcon(new javax.swing.ImageIcon(getClass().getResource("/gui/deck/"+ i +"D.png"))); 
            }
        }
        
    }//GEN-LAST:event_slider_suitStateChanged


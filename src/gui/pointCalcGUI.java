    private void b_calcPointsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_calcPointsActionPerformed
        // TODO add your handling code here:
        hand.setCut(hand.get(0));
        CalcCribPoints calc = new CalcCribPoints(hand);
        HashMap<String,Integer> points = calc.getPoints();
        
        String dp = ""; //displayPoints in txt_points
        dp += "Nobs:" + points.get("nob") + "\t";
        dp += "Nibs:" + points.get("nib") + "\n";
        dp += "Flush:" + points.get("suit") + "\t";
        dp += "Pairs:" + points.get("pair") + "\n";
        dp += "Runs:" + points.get("run") + "\t";
        dp += "Fifteens:" + points.get("fifteen") + "\n";
        dp += "Total:" + points.get("total");
        
        txt_points.setText(dp);
        
    }//GEN-LAST:event_b_calcPointsActionPerformed


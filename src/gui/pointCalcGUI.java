    //for displaying the textual representation of the card in the hand text field
    private void displayHand(String card){
        switch(global_suit){
            case "Spades":
                txt_hand.setText(txt_hand.getText() + card + "♠, ");
                break;
            case "Hearts":
                txt_hand.setText(txt_hand.getText() + card + "♥, ");
                break;
            case "Clubs":
                txt_hand.setText(txt_hand.getText() + card + "♣, ");
                break;
            case "Diamonds":
                txt_hand.setText(txt_hand.getText() + card + "♦, ");
                break;
        }
    }
        
    
